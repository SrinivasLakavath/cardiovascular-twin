def icon(name, size=20, color='currentColor'):
    """
    Return an inline SVG icon.
    
    Parameters
    ----------
    name : str
        Icon name (see ICONS dict for available names)
    size : int
        Icon size in pixels
    color : str
        CSS color value
    
    Returns
    -------
    str
        HTML string with inline SVG
    """
    if name not in ICONS:
        return ''
    return _svg(ICONS[name], size=size, color=color)


def icon_text(icon_name, text, size=20, color='currentColor', text_style=''):
    """Return icon + text as an inline HTML span."""
    return (
        f'<span style="display:inline-flex; align-items:center; {text_style}">'
        f'{icon(icon_name, size, color)}'
        f'<span>{text}</span></span>'
    )
